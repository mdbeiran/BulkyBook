// Lock or Unlock User
function LockUnlock(id) {
    $.ajax({
        type: "POST",
        url: "/Admin/ManageUser/LockUnlock",
        data: JSON.stringify(id),
        contentType: "application/json",
        success: function (data) {
            if (data.success) {
                toastr.success(data.message);
                dataTable.ajax.reload();
            }
            else {
                toastr.error(data.message);
            }
        }
    });
}